// SOMA (+)

let primeiroValor = 4;
let segundoValor = 2;

console.log(primeiroValor + segundoValor); 

let number = 5;
let string = "6";

console.log(number + string); 


// SUBTRAÇÃO (-)

console.log(primeiroValor - segundoValor); // O resultado será o número 2

// DIVISÃO (/)

console.log(primeiroValor / segundoValor); // O resultado será o número 2

// MULTIPLICAÇÃO (*)

console.log(primeiroValor * segundoValor); // O resultado será o número 8

// RESTO DE UMA DIVISÃO (%)

console.log(primeiroValor % segundoValor); // O resultado será o número 0
